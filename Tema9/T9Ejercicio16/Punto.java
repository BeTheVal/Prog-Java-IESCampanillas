package T9Ejercicio16;

public class Punto {
    private double corX;
    private double corY;

    public Punto(double corX, double corY) {
        this.corX = corX;
        this.corY = corY;
    }

    public double getCorX() {
        return corX;
    }

    public double getCorY() {
        return corY;
    }
}
